$(function(){
    var $page = $('html, body');
    $('a[href*="#"]').click(function() {
        $page.animate({
            scrollTop: $($.attr(this, 'href')).offset().top
        }, 400);
        return false;
    });

    $('.menu-btn').on('click', function(){
        $('.menu__list').toggleClass('active')
    });
});